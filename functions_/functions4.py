def create_name(first, last):
    
    first= first.capitalize()
    last= last.capitalize()
    
    return first + " " + last


full_name = create_name("khalid", "mahmood")
full_name1 = create_name("taaha", "ahmed")
full_name2 = create_name("hummzah", "siddiqui")

print(full_name)
print(full_name1)
print(full_name2)
    